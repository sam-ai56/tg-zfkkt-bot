const yanukevich = require("./yanukevich");
const menu = require("./menu");

function open_menu(menu_name) {
    yanukevich.print_anekdot();
    menu.list.forEach(menu => {
        if(menu.name != menu_name)
            return;
        menu.init().then(ret => {
            if(ret == undefined)
                return;
            open_menu(ret);
        });
    });
}

menu.load().then(() => {
    open_menu("main_menu");
});